const mongoose = require("mongoose");

const userSchema = new mongoose.Schema({
  username: {
    type: String,
    required: true,
    unique: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  profilePicture: {
    type: String, // Store the path to the profile picture
    default: "", // or a default profile picture URL
  },
});

module.exports = mongoose.model("User", userSchema);
